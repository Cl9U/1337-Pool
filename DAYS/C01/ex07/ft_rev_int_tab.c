void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	swp;

	i = 0;
	j = size - 1;
	while (i < j)
	{
		swp = tab[i];
		tab[i] = tab[j];
		tab[j] = swp;
		i++;
		j--;
	}
}
